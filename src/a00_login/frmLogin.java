    private void hacerLogin(){
        Login login = new Login();
        String usuario = txtUsuario.getText();
        String password = String.valueOf(txtPassword.getPassword());
        Usuario logueo = null;
        JFrame jf;
        try {
            logueo = login.login(usuario, password);
            if (logueo.getTipoUsuario().equals("Atencion al Cliente")){
                jf = new PrincipalCliente();
                jf.setVisible(true);
                this.setVisible(false);
            } else if (logueo.getTipoUsuario().equals("Jefe Produccion")){
                jf = new PrincipalJefeProduccion();
                jf.setVisible(true);
                this.setVisible(false);
            } else if (logueo.getTipoUsuario().equals("Asistente Compras")) {
                jf = new Principal_Asis();
                jf.setVisible(true);
                this.setVisible(false);
            } else if (logueo.getTipoUsuario().equals("Proveedor")) {
                jf = new Principal_proveedor();
                jf.setVisible(true);
                this.setVisible(false);
            } else if (logueo.getTipoUsuario().equals("Operario Almacen")) {
                jf = new PrincipalOpAlm();
                jf.setVisible(true);
                this.setVisible(false);
            } else if (logueo.getTipoUsuario().equals("Jefe Compras")) {
                jf = new PrincipalJefeCompras();
                jf.setVisible(true);
                this.setVisible(false);
            } else if (logueo.getTipoUsuario().equals("Inspector Calidad")) {
                extraccion_tablas et = new extraccion_tablas();
                System.out.println(usuario +" : "+password);
                int n = et.ObtenerIDUsuario(usuario, password);
                System.out.println(n);
                PrincipalInspectorCalidad pop = new PrincipalInspectorCalidad();
                pop.setIDOperarioProduccion(n);
                jf = pop;
                jf.setVisible(true);
                this.setVisible(false);
            } else if (logueo.getTipoUsuario().equals("Jefe Calidad")) {
                jf = new PrincipalJefeCalidad();
                jf.setVisible(true);
                this.setVisible(false);
            } else if (logueo.getTipoUsuario().equals("Jefe Area Contable")) {
                jf = new PrincipalJefeContable();
                jf.setVisible(true);
                this.setVisible(false);
            } else if(logueo.getTipoUsuario().equals("Operario Produccion")){//
                System.out.println("en elaboracion");
            }
            else {
                JOptionPane.showMessageDialog(null,"Error en los datos de login","Login Error",JOptionPane.QUESTION_MESSAGE);
            }
        } catch (SQLException ex) {
            Logger.getLogger(frmLogin.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
